// Criando uma pilha usando um array
let pilha = [];

// Adicionando elementos à pilha (push)
pilha.push("A");
pilha.push("B");
pilha.push("C");

// A pilha agora contém: ["A", "B", "C"]
console.log("Pilha atual:", pilha);

// Removendo o elemento do topo da pilha (pop)
let topo = pilha.pop();
console.log("Elemento removido:", topo);

// A pilha agora contém: ["A", "B"]
console.log("Pilha após remoção:", pilha);